SUBROUTINE DumpConfig2(iunit,iniocc,inival,nq,nk,no,nohole)
! Write configuration to file (meant to be called from outside the module, when ionicity and core hole/screening have been added for all atoms)
integer,intent(in) :: iunit
integer,intent(in) :: no(0:nph+1) !=norb
real*8,intent(in) ::  iniocc(41,0:nph+1),inival(41,0:nph+1)  !arrays representing only occupied orbitals (i.e. xnel,xnval)  -- iz and nph are known already
real*8            ::  intocc(41,0:nph),intval(41,0:nph)  !arrays representing all orbitals, including empty ones
integer,intent(in) :: nq(41,0:nph+1),nk(41,0:nph+1)
integer,intent(in) :: nohole  ! <0 : FSR corehole ; =0 : ground state ; >0 : RPA corehole
integer iph,i,j,k,iloop,iloop2,idebug
logical, parameter :: write_spin = .false.  !ispn is not currently part of the input
!write(*,*) 'DUMPCONFIG2*****************'
idebug=0
!write(*,*) 'nk',nk(:,idebug)
!write(*,*) 'nq',nq(:,idebug)
!write(*,*) 'xnel',iniocc(:,1)
!write(*,*) 'norb',no(idebug)
! Expand arrays of occupied states occupations to arrays of occupations for all orbitals
intocc=0.d0
intval=0.d0
do i=0,nph
    !In a COREHOLE none/RPA calculation, some of the core hole information is found in column nph+1.  *Sigh*  Look what a wonderful mess we have here:
	if (i.eq.0 .and. nohole.ge.0) then
	   iloop = nph+1
	   if (nohole.eq.0) then
	      iloop2 = i
	   else
	      iloop2 = iloop
	   endif
	else
	   iloop = i
	   iloop2 = i
	endif
	do j=1,no(iloop)
	   do k=1,40
		  if (nnum(k).eq.nq(j,iloop) .and. kappa(k).eq.nk(j,iloop)) then
				intocc(k,i)=iniocc(j,iloop2)
				intval(k,i)=inival(j,iloop2)
		  endif
	   enddo
	enddo
enddo

! Write to file
open(iunit,file='config.dat',form='formatted',status='unknown')
write(iunit,'(a)') '# Configuration of all atom types in feff.inp.'
write(iunit,'(a88)') '# Atomic occupation numbers including core hole, screening, and ionicity (but no SCF).'
if (write_spin) then
   write(iunit,'(a)') '# iph, z,name,  iocc/ival/ispn (i=1,40)'
else 
   write(iunit,'(a)') '# iph, z,name,  iocc/ival (i=1,40)'
endif
do iph=0,nph
   write(iunit,'(i3,2x,i3,2x,a2,2x,40(f5.2,2x))') iph,iz(iph),element(iz(iph)),intocc(1:40,iph)
   write(iunit,'(14x,40(f5.2,2x))') intval(1:40,iph)
   if(write_spin) write(iunit,'(14x,40(f5.2,2x))') ispn(iph,:)
enddo
close(iunit) 
return
end subroutine DumpConfig2


